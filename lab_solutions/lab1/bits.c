int leastBitPos(int x) {
  int result;

  result = (~x + 1) & x;

  return result;
}
/* 
 * logicalShift - shift x to the right by n, using a logical shift
 *   Can assume that 1 <= n <= 31
 *   Examples: logicalShift(0x87654321,4) = 0x08765432
 *   Legal ops: ~ & ^ | + << >>
 *   Max ops: 16
 *   Rating: 3 
 */
int logicalShift(int x, int n) {
  int mask, result;
  
  // first create a mask with (32-n)s 0
  mask = ~((0x1 << 31) >> n << 1);
  result = (x >> n) & mask;

  return result;
}
/*
 * satAdd - adds two numbers but when positive overflow occurs, returns
 *          maximum possible value, and when negative overflow occurs,
 *          it returns minimum positive value.
 *   Examples: satAdd(0x40000000,0x40000000) = 0x7fffffff
 *             satAdd(0x80000000,0xffffffff) = 0x80000000
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 30
 *   Rating: 4
 */
int satAdd(int x, int y) {
  int sum, mask, temp, result;

  // I assume when negative overflow occurs, returns minimum *possible* value (according to the example given)
  sum = x + y;
  mask = ((sum ^ x) & (sum ^ y)) >> 31; // 0xff.. if overflow(due to sign change after adding), 0x0 else
  temp = sum & ~mask; // 0x0 if overflow, sum else
  result = (mask & ((1 << 31) + (sum >> 31))) | temp; // max and min for result (see top comment for min value)

  return result;
}
/* 
 * tc2sm - Convert from two's complement to sign-magnitude 
 *   where the MSB is the sign bit
 *   You can assume that x > TMin
 *   Example: tc2sm(-5) = 0x80000005.
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 15
 *   Rating: 4
 */
int tc2sm(int x) {
  int checkSign, mask, result;

  checkSign = x >> 31;
  mask = 0x1 << 31;
  result = (~checkSign & x) | (checkSign & (~(x + ~0x0) | mask));

  return result;
}
